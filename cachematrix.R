## the calculations will only occur if the object was just initialized and
## the inverse hasn't been calculated and "cached"


##  argumrent x is an matrix/object created with makeCacheMatrix that contains
## the matrix and functions associated with the object
cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
  
      ## check object and use function to retrieve the inverted matrix
      ## or NULL if it has not yet been calculated
  
      matrixInv <- x$getinv()
      
      ## if returned value is not NULL meaning that the inverse has been 
      ## calculated and stored within the object then exit function and return 
      ## inversed matrix
      if (!is.null(matrixInv)){
            message("getting cached data")
            return(matrixInv)
      }
      ## creaate local var and populate with matrix from object x$get call
      mymat.data <- x$get()
      
      ## solve matrix and store in object from makeCacheMatrix (actual cache event)
      matrixInv <- solve(mymat.data,...)
      x$setinv(matrixInv)
      ## return value
      matrixInv
}
