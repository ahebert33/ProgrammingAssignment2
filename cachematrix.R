## This protocol will create a matrix that is a user defined object with both
## "data" and operators associated with the data 

## the matrix information will be the parameter passed to the 
##  function makeCacheMatrix()  "x"
## 
## this protocol must be used to create the object for cachesolve() 

makeCacheMatrix <- function(x = matrix()) {
      ## variable to store matrix inverse results - persist within object
      CachedInv <- NULL
      
      ################################
      ## a group of functions that allow calls to:
      ## set and get the matrix to be inversed
      ## set only will work after a call to makeCacheMatrix
      ## set() is not called in this excersize
      
      set <- function(y) {
            x <<- y
            CachedInv <<- NULL
      }
      get <- function() x
      
      ## set and get the inversed function var CachedInv
      setinv <- function(inverse) CachedInv <<- inverse
      getinv <- function() CachedInv
      #################################################
      
      ## returns a list with named members
      list(set = set, get = get, setinv = setinv, getinv = getinv)

}


